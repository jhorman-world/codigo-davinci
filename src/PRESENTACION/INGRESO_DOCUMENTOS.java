    private void btn_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_guardarActionPerformed
        int id=0;
        validar_campos();
        if (JOptionPane.showConfirmDialog(rootPane, "¿Desea guardar la información?","Confirmación de guardado",JOptionPane.WARNING_MESSAGE,
            JOptionPane.YES_NO_OPTION)==JOptionPane.YES_OPTION){
            
            id= doc_ing.Ultimo_Id_tramite()+1;
            String fecha= Date_a_String(txt_f_ingreso.getDate());
            String fecha_venc = Date_a_String(sumarDiasFecha(txt_f_ingreso.getDate(),3));
            
            
            doc_ing.Guardar_administrado(id,txt_nombre.getText(), txt_apellido.getText(), txt_direccion.getText());
            doc_ing.Guardar_monto(id,cb_categoria.getSelectedItem().toString(), Double.parseDouble(txt_monto.getText()));
            doc_ing.Guardar_doc_ingreso(id, Integer.parseInt(txt_nro_folios.getText()), fecha, (String)cb_doc_ingreso.getSelectedItem() ,txt_detalle_documentoing.getText() , txt_asunto.getText());
            doc_ing.Guardar_plazo(id, fecha, fecha_venc);
            doc_ing.Guardar_tramite(id, Integer.parseInt(txt_exp.getText()), Integer.parseInt(lbl_año_REG.getText()));
            metodosbd.guardar_proveido("sin proveido",txt_reg.getText());
            Limpiar_campos();
            txt_reg.setText(String.valueOf(doc_ing.Ultimo_Id_tramite()+1));
            
        }
    }//GEN-LAST:event_btn_guardarActionPerformed

