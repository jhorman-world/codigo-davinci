 public Date sumarDiasFecha(Date fecha, int dias){

      Calendar calendar = Calendar.getInstance();
	
      calendar.setTime(fecha); // Configuramos la fecha que se recibe
	
      calendar.add(Calendar.DAY_OF_YEAR, dias);  // numero de días a añadir, o restar en caso de días<0

      return calendar.getTime(); // Devuelve el objeto Date con los nuevos días añadidos
	
 }
    
    private String Date_a_String(Date fecha){
        Calendar c = Calendar.getInstance();
        c.setTime(fecha);

        String dia = Integer.toString(c.get(Calendar.DATE));
        String mes = Integer.toString((c.get(Calendar.MONTH))+1);
        String annio = Integer.toString(c.get(Calendar.YEAR));
        return annio+"-"+(mes)+"-"+dia;     
}
    
    private  boolean isNumeric(String cadena){
	try {
		Integer.parseInt(cadena);
		return true;
	} catch (NumberFormatException nfe){
		return false;
	}
    }
    
    private boolean validar_campos (){
        
            if (txt_reg.getText().equals("") ) {
                JOptionPane.showMessageDialog(null, "Debe llenar el campo NRO. DE REGISTRO CATASTRO para continuar");
                return false;
            }
            
            if (!isNumeric(txt_reg.getText()) ) {
                JOptionPane.showMessageDialog(null, "Debe llenar datos válidos en el campo NRO. DE REGISTRO CATASTRO para continuar");
                return false;
            }
            
            if (txt_exp.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "Debe llenar el campo EXP/MP para continuar");
                return false;
            }
            if (!isNumeric(txt_exp.getText()) ) {
                JOptionPane.showMessageDialog(null, "Debe llenar datos válidos en el campo EXP/MP para continuar");
                return false;
            }
            if (txt_nro_folios.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "Debe llenar el campo NRO FOLIOS para continuar");
                return false;
            }
            
            if (!isNumeric(txt_nro_folios.getText()) ) {
                JOptionPane.showMessageDialog(null, "Debe llenar datos válidos en el campo NRO FOLIOS para continuar");
                return false;}
            
            if (txt_f_ingreso.getDate().toString().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Debe llenar el campo FECHA DE INGRESO para continuar");
                return false;
            }
            if (txt_detalle_documentoing.getText().equals("")) {
                JOptionPane.showMessageDialog(null,"Debe llenar el campo DOCUMENTO DE INGRESO para continuar");
                return false;
            }
            if (txt_nombre.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "Debe llenar el campo NOMBRE para continuar");
                return false;
            }
            
            if (txt_apellido.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "Debe llenar el campo APELLIDO para continuar");
                return false;
            }
            
            if (txt_direccion.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "Debe llenar el campo DIRECCION para continuar");
                return false;
            }
            
            if (pnl_oposicion.isVisible()) {
                if (txt_opo_direcc.getText().equals("") && txt_opo_nombre.getText().equals("")&& txt_opo_reg.getText().equals("")) {
                    JOptionPane.showMessageDialog(null, "Debe llenar alguno de los campos de OPOCISIÓN para continuar");
                    return false;
                    
                }
            }
            
        
            return true;
        
    }

